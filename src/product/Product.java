package product;

public class Product {
	
	String description;
	String code;
	String typeCode; 
	String brand;
	String suggestedBrand;
	String category;
	String suggestedCategory;
	String unit;
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getTypeCode() {
		return typeCode;
	}
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getSuggestedBrand() {
		return suggestedBrand;
	}
	public void setSuggestedBrand(String suggestedBrand) {
		this.suggestedBrand = suggestedBrand;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getSuggestedCategory() {
		return suggestedCategory;
	}
	public void setSuggestedCategory(String suggestedCategory) {
		this.suggestedCategory = suggestedCategory;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
		

}
